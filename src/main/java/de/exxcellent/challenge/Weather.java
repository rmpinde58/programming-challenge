package de.exxcellent.challenge;

public class Weather {
    private String day;
    private String mxT;
    private String mnT;

    public Weather() {
    }

    public String getDay() {
        return day;
    }

    public void setDay(String day) {
        this.day = day;
    }

    public String getMxT() {
        return mxT;
    }

    public void setMxT(String mxT) {
        this.mxT = mxT;
    }

    public String getMnT() {
        return mnT;
    }

    public void setMnT(String mnT) {
        this.mnT = mnT;
    }
}
